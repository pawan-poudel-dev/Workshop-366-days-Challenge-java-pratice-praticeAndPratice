//print number 1 to 100
public class PrintNumber {
    public static void main(String [] args){
        // variable i must be initialize before using the loop
        int i = 1;
        // using the while loop to print nuymbers from 1 to 100
        while(i <= 100){
            System.out.println("Numbers from 1 to 100 are:"+i);
            // use increment to increase value of i by 1
            i++;

        }
    }
}
